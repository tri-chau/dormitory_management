private void loadData(){
    String manv = DangNhap.GlobalVariables.username;
    Connection con = QLKTX.getConnection();
    if(con == null) {
        JOptionPane.showMessageDialog(this,"Lỗi kết nối csdl","Lỗi",JOptionPane.ERROR_MESSAGE);
        return;
    }
        //tạo 1 table mới
        DefaultTableModel table = new DefaultTableModel();
        //gán cho table cột
        String title[] = {"Mã vi phạm","MSSV","Nội dung","Ngày vi phạm","Ngày lập vi phạm"};
        table.setColumnIdentifiers(title);
        try{                        
                String sql = "SELECT * FROM PHIEUVIPHAM P JOIN CUTRU C ON P.MSSV = C.MSSV"
                        + " JOIN PHONG ON C.MAPHONG=PHONG.MAPHONG"
                        + " JOIN TOA T ON PHONG.MATOA = T.MATOA"
                        + " WHERE T.MATRUONGTOA = ? ORDER BY MAVIPHAM";
                PreparedStatement pst = con.prepareStatement(sql);  
                pst.setString(1, manv);
                ResultSet rs = pst.executeQuery();
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                while(rs.next()){
                    String MaVP = rs.getString("MAVIPHAM");
                    String MSSV = rs.getString("MSSV");
                    String NoiDung = rs.getString("NOIDUNG");
                    Date NgayVP = rs.getDate("NGAYVIPHAM"); 
                    String ngayvp = sdf.format(NgayVP);
                    Date NgayLapVP = rs.getDate("NGAYLAPVP");
                    String ngaylapvp = sdf.format(NgayLapVP);
                    table.addRow(new Object[]{MaVP,MSSV,NoiDung,ngayvp,ngaylapvp});
                }
                
                //gán cho table chính
                tbDSViPham.setModel(table);
                //cho giá trị xuất hiện giữa bảng
                DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
                centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
                for (int i = 0; i < tbDSViPham.getColumnCount(); i++) {
                    tbDSViPham.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
                }  
                pst.close();
                rs.close();
                con.close();                     
        }catch(SQLException e) {
            e.printStackTrace();
        }
    }
