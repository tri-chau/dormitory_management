    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
     
        String mssv = txtMSSV.getText();
        String noidung = txtNoiDung.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        String ngayvp = sdf.format(txtNgayVP.getDate());
        String manv = DangNhap.GlobalVariables.username;
        if(mssv.equals("") || noidung.equals("") || ngayvp.equals("")){
            JOptionPane.showMessageDialog(this, "Điền đầy đủ thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Connection con = QLKTX.getConnection();
        if(con == null) {
            JOptionPane.showMessageDialog(this,"Lỗi kết nối csdl","Lỗi",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String sql = "{CALL THEM_VIPHAM(?,?,?,?,?,?)}";
        try{                
                CallableStatement cst = con.prepareCall(sql);
                cst.registerOutParameter(1,java.sql.Types.VARCHAR);
                cst.setString(2,manv);
                cst.setString(3,mssv);
                cst.setString(4,noidung);
                cst.setString(5,ngayvp);   
                cst.registerOutParameter(6,java.sql.Types.VARCHAR);
                cst.execute();
                
                String mavp = cst.getString(1);
                String ngaylapvp = cst.getString(6);
                DefaultTableModel model = (DefaultTableModel)tbDSViPham.getModel();
                model.addRow(new Object[]{mavp,mssv,noidung,ngayvp,ngaylapvp});
                cst.close();
                con.close();
                
        }
        catch(SQLException e){
            e.printStackTrace();
            StringBuilder errorMessage = new StringBuilder();
                    errorMessage.append("Message: ").append(e.getMessage()).append("\n");
                    JOptionPane.showMessageDialog(null, errorMessage.toString(), "Lỗi cơ sở dữ liệu", JOptionPane.ERROR_MESSAGE);
        }              
        txtMSSV.setText("");
        txtNoiDung.setText("");
        txtNgayVP.setDate(null);   
    }//GEN-LAST:event_btThemActionPerformed

