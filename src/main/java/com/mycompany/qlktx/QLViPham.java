    private void btLocVPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLocVPActionPerformed
        String manv = DangNhap.GlobalVariables.username;
    SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
    String loai = (String) cbLoai.getSelectedItem();
    String filterLoai = txtCbTim.getText();
    String tungay = dcTuNgay.getDate() != null ? sdf.format(dcTuNgay.getDate()) : "";
    String denngay = dcDenNgay.getDate() != null ? sdf.format(dcDenNgay.getDate()) : "";

    // Kiểm tra nếu không có điều kiện lọc nào thì tải lại dữ liệu
    if (filterLoai.isEmpty() && tungay.isEmpty() && denngay.isEmpty()) {
        loadData();
        return;
    }

    Connection con = QLKTX.getConnection();
    if (con == null) {
        JOptionPane.showMessageDialog(this, "Lỗi kết nối csdl", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Tạo 1 table mới
    DefaultTableModel table = new DefaultTableModel();
    // Gán cho table cột
    String title[] = {"Mã vi phạm", "MSSV", "Nội dung", "Ngày vi phạm", "Ngày lập vi phạm"};
    table.setColumnIdentifiers(title);

    try {
        StringBuilder sql = new StringBuilder("SELECT * FROM PHIEUVIPHAM P JOIN CUTRU C ON P.MSSV = C.MSSV"
                + " JOIN PHONG ON C.MAPHONG = PHONG.MAPHONG"
                + " JOIN TOA T ON PHONG.MATOA = T.MATOA"
                + " WHERE T.MATRUONGTOA = ?");

        if (!filterLoai.isEmpty()) {
            if ("MSSV".equals(loai)) {
                sql.append(" AND P.MSSV = ?");
            } else if ("Mã vi phạm".equals(loai)) {
                sql.append(" AND P.MAVIPHAM = ?");
            }
        }

        if (!tungay.isEmpty() && denngay.isEmpty()) {
            sql.append(" AND P.NGAYLAPVP >= TO_DATE(?, 'DD-MM-YYYY')");
        } else if (tungay.isEmpty() && !denngay.isEmpty()) {
            sql.append(" AND P.NGAYLAPVP<= TO_DATE(?, 'DD-MM-YYYY')");
        } else if (!tungay.isEmpty() && !denngay.isEmpty()) {
            sql.append(" AND P.NGAYLAPVPBETWEEN TO_DATE(?, 'DD-MM-YYYY') AND TO_DATE(?, 'DD-MM-YYYY')");
        }

        PreparedStatement pst = con.prepareStatement(sql.toString());
        pst.setString(1, manv);
        int index = 2;

        if (!filterLoai.isEmpty()) {
            pst.setString(index++, filterLoai);
        }

        if (!tungay.isEmpty() && denngay.isEmpty()) {
            pst.setString(index++, tungay);
        } else if (tungay.isEmpty() && !denngay.isEmpty()) {
            pst.setString(index++, denngay);
        } else if (!tungay.isEmpty() && !denngay.isEmpty()) {
            pst.setString(index++, tungay);
            pst.setString(index++, denngay);
        }
        ResultSet rs = pst.executeQuery();
        while (rs.next()) {
            String maVP = rs.getString("MAVIPHAM");
            String mssv = rs.getString("MSSV");
            String noiDung = rs.getString("NOIDUNG");
            Date ngayViPham = rs.getDate("NGAYVIPHAM");
            Date ngayLapVP = rs.getDate("NGAYLAPVP");
            String ngayvp = sdf.format(ngayViPham);
            String ngaylapvp = sdf.format(ngayLapVP);

            table.addRow(new Object[]{maVP, mssv, noiDung, ngayvp, ngaylapvp});
        }
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
                centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
                for (int i = 0; i < tbDSViPham.getColumnCount(); i++) {
                    tbDSViPham.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
                }
        // Gán dữ liệu mới vào bảng
        tbDSViPham.setModel(table);

        rs.close();
        pst.close();
        con.close();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi trong quá trình truy vấn cơ sở dữ liệu", "Lỗi", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btLocVPActionPerformed

