    private void layDSThongBao() {
        String ndTimKiem = txtThongBao.getText();
        
        Connection con = getConnection();
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
        }
            
        try {           
            StringBuilder sql = new StringBuilder("SELECT MATHONGBAO, NOIDUNG FROM THONGBAO WHERE MANGUOINHAN IS NULL");
            if (!ndTimKiem.isEmpty()) {
                ndTimKiem = "%" + ndTimKiem + "%";
                sql.append(" AND NOIDUNG LIKE ?");
            }
            sql.append(" ORDER BY MATHONGBAO");
                       
            
            PreparedStatement pstmt = con.prepareStatement(sql.toString());
            if (!ndTimKiem.isEmpty()) {
                pstmt.setString(1, ndTimKiem);
            }
            ResultSet rs = pstmt.executeQuery();
            
            tbThongBao.setRowCount(0); // Clear table
            while (rs.next()) {
                String maTB = rs.getString("MATHONGBAO");
                String ndTB = rs.getString("NOIDUNG");
                tbThongBao.addRow(new Object[]{maTB, ndTB});
            }
            
            rs.close();
            pstmt.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi trong quá trình truy vấn cơ sở dữ liệu1", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

