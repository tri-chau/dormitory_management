    public void layHoaDon() {
        Connection con = QLKTX.getConnection();
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
        }
            
        try {           
            StringBuilder sql = new StringBuilder("SELECT * FROM HOADON WHERE MAHOADON = ?");
            PreparedStatement pstmt = con.prepareStatement(sql.toString());
            pstmt.setString(1, maHD);

            ResultSet rs = pstmt.executeQuery();
            
            while (rs.next()) {
                String maHoaDon = rs.getString("MAHOADON");
                String maP = rs.getString("MAPHONG");
                String maSSV = rs.getString("MSSV");
                String loaiHD = rs.getString("LOAIHOADON");
                String tienHD = rs.getString("SOTIEN");
                String nLHD = QLKTX.convertDateFormat(rs.getString("NGAYLAPHOADON"));
                String hanHD = QLKTX.convertDateFormat(rs.getString("HANTHANHTOAN"));
                String ngTT = QLKTX.convertDateFormat(rs.getString("NGAYTHANHTOAN"));
                
                txtMaHD.setText(maHoaDon);
                txtLoaiHD.setText(loaiHD);
                txtMaPhong.setText(maP);
                txtMSSV.setText(maSSV);
                txtGiaHD.setText(tienHD);
                txtLHD.setText(nLHD);
                txtHanTT.setText(hanHD);
                txtNTT.setText(ngTT);
            }
            rs.close();
            pstmt.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi trong quá trình truy vấn cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
