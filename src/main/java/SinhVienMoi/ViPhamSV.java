    public void layDSViPham() {
        String ndTimKiem = txtTimKiem.getText();
        
        Connection con = getConnection();
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
        }
            
        try {           
            StringBuilder sql = new StringBuilder("SELECT NOIDUNG, NGAYVIPHAM, NGAYLAPVP FROM PHIEUVIPHAM WHERE MSSV = ?");
            if (!ndTimKiem.isEmpty()) {
                ndTimKiem = "%" + ndTimKiem + "%";
                sql.append(" AND NOIDUNG LIKE ?");
            }
            sql.append(" ORDER BY MAVIPHAM");
            
            PreparedStatement pstmt = con.prepareStatement(sql.toString());
            pstmt.setString(1, mssv);
            if (!ndTimKiem.isEmpty()) {
                pstmt.setString(2, ndTimKiem);
            }
            ResultSet rs = pstmt.executeQuery();
            
            tbVipham.setRowCount(0); // Clear table
            tbNDVP.setRowCount(0);
            while (rs.next()) {
                String ngayVP = convertDateFormat(rs.getString("NGAYVIPHAM"));
                String ngayLVP = convertDateFormat(rs.getString("NGAYLAPVP"));
                String ndVP = rs.getString("NOIDUNG");
                tbVipham.addRow(new Object[]{ndVP, ngayVP, ngayLVP});
            }
            rs.close();
            pstmt.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi trong quá trình truy vấn cơ sở dữ liệu1", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void hienThiChiTietViPham(String maViPham) {
        Connection con = getConnection();
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            String sql = "SELECT * FROM PHIEUVIPHAM WHERE MAVIPHAM = ?";
            PreparedStatement pstmt = con.prepareStatement(sql);
            pstmt.setString(1, maViPham);
            ResultSet rs = pstmt.executeQuery();

            tbNDVP.setRowCount(0); // Clear table
            while(rs.next()) {
                
                String ndVP = rs.getString("NOIDUNG");
                tbNDVP.addRow(new Object[]{ndVP});
            }
            rs.close();
            pstmt.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi trong quá trình truy vấn cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
