    private void layTTSV(String mssv) {
        Connection con = getConnection();
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy"); 
        System.out.println(mssv);
        try {         
            String sql1 = "SELECT * " +
                     "FROM SINHVIEN S JOIN CUTRU C ON S.MSSV = C.MSSV " +
                     "WHERE S.MSSV = ? AND TINHTRANGO = 0 ";
            PreparedStatement pstmt = con.prepareStatement(sql1);
            pstmt.setString(1, mssv);
            ResultSet rs = pstmt.executeQuery();
            
            while (rs.next()) {
                String tenSv = rs.getString("TENSV");
                String ngSinh = convertDateFormat(rs.getString("NGSINH"));
                int gioitinh = rs.getInt("GIOITINH");
                String gioitinhS;
                if(gioitinh == 0)
                    gioitinhS = "Nữ";
                else if(gioitinh == 1);
                    gioitinhS = "Nam";
                String email = rs.getString("EMAIL");
                String sdt = rs.getString("SDT");                 
                String maPhong = rs.getString("MAPHONG");
                Date tungay = rs.getDate("TUNGAY");
                Date denngay = rs.getDate("DENNGAY");
                String tungayS = sdf.format(tungay);
                
                String sql = "SELECT TINHTRANGHD FROM HOADON WHERE MSSV = ? AND MAPHONG = ? AND TUNGAY = TO_DATE(?,'DD-MM-YYYY')";
                PreparedStatement pstmt2 = con.prepareStatement(sql);
                pstmt2.setString(1, mssv);
                pstmt2.setString(2, maPhong);
                pstmt2.setDate(3, tungay);
                ResultSet rs2 = pstmt2.executeQuery();
                
                String trangthaiHD = "";
                if(rs2.next()){
                    if(rs2.getInt("TINHTRANGHD") == 1)
                    {
                        trangthaiHD = "Đã thanh toán";
                    }                  
                    else
                        trangthaiHD = "Chưa thanh toán";
                }
                
                
                txtMSSV.setText(mssv);
                txtHoTen.setText(tenSv);
                txtNgaySinh.setText(ngSinh);
                txtGioiTinh.setText(gioitinhS);
                txtEmail.setText(email);
                txtSDT.setText(sdt);
                txtPhong.setText(maPhong);
                txtTungay.setText(tungayS);
                txtDenngay.setText(sdf.format(denngay));
                txtTrangThaiHD.setText(trangthaiHD);
            }
            rs.close();
            pstmt.close();
            con.close();
            
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi trong quá trình truy vấn cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
        }
     }

