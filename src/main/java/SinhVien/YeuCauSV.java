    public void layDSYeuCau() {
        String loaiYCFilter = (String) cbLoaiYC.getSelectedItem();
        String trangThaiFiler = (String) cbTrangThai.getSelectedItem();
        String trangThaiIndex;
        if("Chua duyet".equals(trangThaiFiler)) {
            trangThaiFiler = "0";
        }
        if("Da duyet".equals(trangThaiFiler)) {
            trangThaiFiler = "1";
        }
        
        Connection con = getConnection();
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
        }
            
        try {           
            StringBuilder sql = new StringBuilder("SELECT MAYEUCAU, LOAIYEUCAU, NOIDUNG, TRANGTHAIXULY FROM PHIEUYEUCAU WHERE MSSV = ?");
            if (!"Default".equals(loaiYCFilter)) {
                sql.append(" AND LOAIYEUCAU = ?");
            }
            if (!"Default".equals(trangThaiFiler)) {
                sql.append(" AND TRANGTHAIXULY = ?");
            }
            sql.append(" ORDER BY MAYEUCAU");

            PreparedStatement pstmt = con.prepareStatement(sql.toString());
            pstmt.setString(1, mssv);

            int parameterIndex = 2;
            if (!"Default".equals(loaiYCFilter)) {
                pstmt.setString(parameterIndex++, loaiYCFilter);
            }
            if (!"Default".equals(trangThaiFiler)) {
                pstmt.setString(parameterIndex++, trangThaiFiler);
            }
            ResultSet rs = pstmt.executeQuery();
            
            tb.setRowCount(0); // Clear table
            while (rs.next()) {
                String maYC = rs.getString("MAYEUCAU");
                
                int loaiYC = rs.getInt("LOAIYEUCAU");
                String loaiycS = "";
                if(loaiYC == 1)
                    loaiycS = "Đổi phòng";
                else if(loaiYC == 2)
                    loaiycS = "Trả phòng";
                else if(loaiYC == 3)
                    loaiycS = "Gia hạn phòng";
                else if(loaiYC == 4)
                    loaiycS = "Sửa chữa";
                else if(loaiYC == 5)
                    loaiycS = "Khiếu nại";
                String ndYC = rs.getString("NOIDUNG");
                String tthaiYC = rs.getString("TRANGTHAIXULY");
                tb.addRow(new Object[]{maYC, loaiycS, ndYC, tthaiYC});
            }
            rs.close();
            pstmt.close();
            con.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi trong quá trình truy vấn cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

