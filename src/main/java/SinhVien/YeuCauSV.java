    private void btTaoYCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTaoYCActionPerformed
        // TODO add your handling code here:
        String loaiYC = (String) cbtaoYC.getSelectedItem();
        String noiDung = txtNoiDung.getText();
        int loaiyc = 0;
        if(loaiYC.equals("Sua chua")){
            loaiyc = 4;
        }
        else if(loaiYC.equals("Khieu nai")){
            loaiyc = 5;
        }

        
        if (noiDung.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nội dung không được để trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int ret = JOptionPane.showConfirmDialog(null, "Xác nhận tạo?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (ret == JOptionPane.YES_OPTION) {
            Connection con = getConnection();
            if (con == null) {
                JOptionPane.showMessageDialog(this, "Lỗi kết nối cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
            }

            try {
                String sql = "{call THEM_PHIEUYEUCAU(?, ?, ?)}";
                CallableStatement cstmt = con.prepareCall(sql);
                cstmt.setString(1, mssv);
                cstmt.setInt(2, loaiyc);
                cstmt.setString(3, noiDung);
                cstmt.execute();
                cstmt.close();
                con.close();

                layDSYeuCau();
                JOptionPane.showMessageDialog(this, "Tạo thành công phiếu yêu cầu", "Success", JOptionPane.INFORMATION_MESSAGE);


            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Lỗi trong quá trình truy vấn cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btTaoYCActionPerformed

